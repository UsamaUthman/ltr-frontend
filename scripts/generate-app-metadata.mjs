import { execFileSync } from 'node:child_process'
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url))
const projectRoot = path.resolve(scriptDirectory, '..')
const packageJsonPath = path.join(projectRoot, 'package.json')
const outputPath = path.join(projectRoot, 'src', 'config', 'app-metadata.ts')

const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'))

let commitHash

try {
  commitHash = execFileSync('git', ['rev-parse', '--short', 'HEAD'], {
    cwd: projectRoot,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'ignore'],
  }).trim()
} catch {
  commitHash = undefined
}

const metadata = {
  name: packageJson.appName ?? packageJson.name,
  version: packageJson.version,
  buildTime: new Date().toISOString(),
  ...(commitHash ? { commitHash } : {}),
}

const generatedSource = `// This file is generated by scripts/generate-app-metadata.mjs.
// Do not edit it manually; update package.json or the generator instead.
export const APP_METADATA = ${JSON.stringify(metadata, null, 2)} as const
`

await mkdir(path.dirname(outputPath), { recursive: true })
await writeFile(outputPath, generatedSource, 'utf8')

console.log(`Generated ${path.relative(projectRoot, outputPath)} for v${metadata.version}`)

